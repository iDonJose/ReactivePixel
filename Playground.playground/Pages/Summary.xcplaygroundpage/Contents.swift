 # ReactivePixel
 ReactiveSwift + UIKit + Texture (AsyncDisplayKit)

 ### Summary

 **Reactive UIKit**
 - [`UIBarButtonItem.wasTapped()`](UIBarButtonItem) : Button tap events
 - [`UIDatePicker.date`](UIDatePicker) : Date value
 - [`UISegmentControl.selectedSegment`](UISegmentControl) : Current selected segment index
 - [`UISlider.value`](UISlider) : Slider's value
 - [`UIStepper.value`](UIStepper) : Stepper's value
 - [`UISwitch.isOn`](UISwitch) : On/Off status
 - [`UITextField.text, UITextField.attributedText`](UITextField) : Text and attributed text on change
 - [`UITextField.editEvents(:)`](UITextField) : TextField's UI events
 - [`UITextView.text, UITextView.attributedText`](UITextView) : Text and attributed text on change
 - [`UITextView.editEvents(:)`](UITextView) : TextView's UI events
 - [`UIView.gesture(:)`](UIView) : Listen to events from a gesture recognizer

 **Reactive Texture**
 - [`ASDisplayNode.gesture(:)`](ASDisplayNode) : Listen to events from a gesture recognizer
 - [`ASEditableTextNode.text, ASEditableTextNode.attributedText`](ASEditableTextNode) : Text and attributed text on change
 - [`ASEditableTextNode.editEvents(:)`](ASEditableTextNode) : EditableText's UI events

